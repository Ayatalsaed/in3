import * as util from 'ethereumjs-util'
import * as Transaction from 'ethereumjs-tx'
import * as Block from 'ethereumjs-block'
import * as Trie from 'merkle-patricia-tree'
import { RPCRequest, RPCResponse } from '../types/config';

export interface Proof {
  type: 'transactionProof',
  block: string,
  merkelProof?: string[],
  txIndex?,
  signature: {
    msgHash: string,
    v: number
    r: string
    s: string
  }
}

export function getBlock(b) {
  const block = new Block({
    header: {
      parentHash: b.parentHash,
      uncleHash: b.sha3Uncles,
      coinbase: b.miner,
      stateRoot: b.stateRoot,
      transactionsTrie: b.transactionsRoot,
      receiptTrie: b.receiptRoot || b.receiptsRoot || util.SHA3_NULL,
      bloom: b.logsBloom,
      difficulty: parseInt(b.difficulty),
      number: b.number,
      gasLimit: b.gasLimit,
      gasUsed: b.gasUsed,
      timestamp: b.timestamp,
      extraData: b.extraData,
      mixHash: b.mixHash,
      nonce: b.nonce
    }
  })

  if (b.transactions)
    block.transactions = b.transactions.map(createTx)

  return block
}

export function blockToHex(block) {
  return getBlock(block).header.serialize().toString('hex')
}
export function blockFromHex(hex) {
  return new Block({ header: util.rlp.decode(util.toBuffer(toHex('0x' + hex))) })
}

export function verifyBlock(b, signature, expectedSigner) {

  // TODO in the future we are not allowing block verification without signature
  if (!signature) return

  const blockHeader = b instanceof Block ? b.header : getBlock(b).header
  const blockHash = '0x' + blockHeader.hash().toString('hex').toLowerCase()
  const messageHash = util.sha3(blockHash + blockHeader.number.toString('hex').padStart(64, '0')).toString('hex')
  if (messageHash !== signature.msgHash)
    throw new Error('The signature signed the wrong message!')
  const signer = '0x' + util.pubToAddress(util.erecover(messageHash, signature.v, util.toBuffer(signature.r), util.toBuffer(signature.s))).toString('hex')
  if (signer.toLowerCase() !== expectedSigner.toLowerCase())
    throw new Error('The signature was not signed by ' + expectedSigner)


  //  if (blockHash !== b.hash.toLowerCase()) throw new Error('BlockHeader invalid! Wrong blockHash!')
}

export async function createTransactionProof(block, txHash, signature): Promise<Proof> {
  const txIndex = block.transactions.findIndex(_ => _.hash === txHash)
  if (txIndex < 0) throw new Error('tx not found')

  // create trie
  const trie = new Trie()
  await Promise.all(block.transactions.map(tx => new Promise((resolve, reject) =>
    trie.put(
      util.rlp.encode(parseInt(tx.transactionIndex)),
      createTx(tx).serialize(),
      error => error ? reject(error) : resolve(true)
    )
  )))

  // check roothash
  if (block.transactionsRoot !== '0x' + trie.root.toString('hex'))
    throw new Error('The transactionHash is wrong! : ' + block.transactionsRoot + '!==0x' + trie.root.toString('hex'))

  // create prove
  return new Promise<Proof>((resolve, reject) =>
    Trie.prove(trie, util.rlp.encode(txIndex), (err, prove) => {
      if (err) return reject(err)
      resolve({
        type: 'transactionProof',
        block: blockToHex(block),
        merkelProof: prove.map(_ => _.toString('hex')),
        txIndex, signature
      })
    }))
}

export async function verifyTransactionProof(txHash: string, proof: Proof, expectedSigner: string) {
  const block = blockFromHex(proof.block)

  verifyBlock(block, proof.signature, expectedSigner)

  // since the blockhash is verified, we have the correct root
  return new Promise((resolve, reject) => {
    Trie.verifyProof(
      block.header.transactionsTrie,
      util.rlp.encode(proof.txIndex),
      proof.merkelProof.map(_ => util.toBuffer('0x' + _)),
      (err, value) => {
        if (err) return reject(err)
        if (txHash === '0x' + util.sha3(value).toString('hex'))
          resolve(value)
        else
          reject(new Error('The TransactionHash could not be verified, since the merkel-proof resolved to a different hash'))
      })
  })


}



export function toHex(val): string {
  if (val === undefined) return undefined
  if (typeof val === 'string')
    return val.startsWith('0x') ? val : '0x' + new util.BN(val).toString(16)
  else if (typeof val === 'number')
    return '0x' + val.toString(16)
  else
    return util.bufferToHex(val)
}


function createTx(transaction) {
  const txParams = {
    ...transaction,
    nonce: toHex(transaction.nonce),
    gasPrice: toHex(transaction.gasPrice),
    value: toHex(transaction.value || 0),
    gasLimit: toHex(transaction.gasLimit === undefined ? transaction.gas : transaction.gasLimit),
    data: toHex(transaction.gasLimit === undefined ? transaction.input : transaction.data),
    to: transaction.to ? util.setLengthLeft(util.toBuffer(transaction.to), 20) : null,
    v: transaction.v < 27 ? transaction.v + 27 : transaction.v
  }
  const fromAddress = util.toBuffer(txParams.from)
  delete txParams.from
  const tx = new Transaction(txParams)
  tx._from = fromAddress
  tx.getSenderAddress = function () { return fromAddress }
  if (txParams.hash !== '0x' + util.sha3(tx.serialize()).toString('hex')) {
    console.log('raw', tx.raw.map(_ => _.toString('hex')))
    throw new Error('wrong txhash! : ' + (txParams.hash + '!== 0x' + util.sha3(tx.serialize()).toString('hex')) + '  full tx=' + tx.serialize().toString('hex'))
  }
  // override hash
  const txHash = util.toBuffer(txParams.hash)
  tx.hash = function () { return txHash }
  return tx
}

export async function verifyProof(request: RPCRequest, response: RPCResponse): Promise<boolean> {
  const proof = response.in3Proof as any as Proof
  if (!proof) return false
  switch (proof.type) {
    case 'transactionProof':
      return verifyTransactionProof(request.params[0], proof, response.w3Node.address).then(_ => true, _ => false)
    default:
      return false
  }
}



